package part3.task8;

public class Task8 {
    public static void main(String[] args) {
        int a = 5;
        int b = 7;
        int r = 6;

        if ((Math.sqrt((a * a) + (b * b))) < r * 2) {
            System.out.println("Отверстие закрыто.");
        } else {
            System.out.println("Отверстие открыто, либо закрыто не полностью.");
        }


    }


}
